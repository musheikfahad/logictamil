package com.logic_first;

public class Loop_control_statements_continue_ex1 {

	public static void main(String[] args) {
		for (int i = 1; i <=10; i++) {
			
			if(i==7)
				continue; // which means once i = 7 skip the remAINING OPERATIONS AND JUMB TO NEXT ITERATION
			System.out.println(i);
			
		}

	}

}
